let number = 0;
let number2 = 10;
// Looping pertama hitungan maju
console.log("Looping Pertama");
while (number <= 10) {
  console.log(number);
  number++;
}
// Looping kedua hitungan mundur
console.log("\n");
console.log("Looping Kedua");
while (number2 > 0) {
  console.log(number2);
  number2--;
}
